// ── Interpolation helpers ─────────────────────────────────────────────────────
function anchorToMonths(anchors) {
  const keys = Object.keys(anchors).sort();
  const points = keys.map(k => {
    const [y, m] = k.split('-').map(Number);
    return { n: y * 12 + m, v: anchors[k] };
  });
  const first = points[0].n;
  const last  = points[points.length - 1].n;
  const result = {};
  for (let n = first; n <= last; n++) {
    const lo = points.filter(p => p.n <= n).pop();
    const hi = points.find(p => p.n >= n);
    if (!lo || !hi || lo.n === hi.n) {
      result[n] = lo ? lo.v : hi.v;
    } else {
      const t = (n - lo.n) / (hi.n - lo.n);
      result[n] = lo.v + t * (hi.v - lo.v);
    }
  }
  return { result, first, last };
}

function nToMonthStr(n) {
  const y = Math.floor((n - 1) / 12);
  const m = ((n - 1) % 12) + 1;
  const MONTHS = ['Jan','Feb','Mar','Apr','May','Jun','Jul','Aug','Sep','Oct','Nov','Dec'];
  return MONTHS[m - 1] + '-' + y;
}

function monthStrToN(s) {
  const MONTHS = ['Jan','Feb','Mar','Apr','May','Jun','Jul','Aug','Sep','Oct','Nov','Dec'];
  const [mon, yr] = s.split('-');
  return parseInt(yr) * 12 + MONTHS.indexOf(mon) + 1;
}

// ── Build projection ──────────────────────────────────────────────────────────
export function buildProjection(purchasePrice, purchaseMonthStr, fredCode) {
  const code    = fredCode || 'CSUSHPISA';
  const anchors = INDEX_ANCHORS[code] || INDEX_ANCHORS.CSUSHPISA;
  const { result: idx, first, last } = anchorToMonths(anchors);

  const purchaseN = monthStrToN(purchaseMonthStr);
  const purchaseIdx = idx[purchaseN];
  if (!purchaseIdx) return [];

  const forecastStart = monthStrToN('Mar-2026');

  return Object.entries(idx).map(([nStr, idxVal]) => {
    const n = parseInt(nStr);
    const value = Math.round((idxVal / purchaseIdx) * purchasePrice);
    const isForecast     = n >= forecastStart;
    const isExtrapolated = n > last;
    return {
      month: nToMonthStr(n),
      value,
      index: idxVal,
      isForecast,
      isExtrapolated,
    };
  });
}

// ── Main handler ──────────────────────────────────────────────────────────────
export default async function handler(req, res) {
  if (req.method === 'OPTIONS') {
    res.setHeader('Access-Control-Allow-Origin', '*');
    res.setHeader('Access-Control-Allow-Methods', 'POST');
    res.setHeader('Access-Control-Allow-Headers', 'Content-Type');
    return res.status(200).end();
  }
  res.setHeader('Access-Control-Allow-Origin', '*');
  if (req.method !== 'POST') return res.status(405).end();

  const { error: authErr } = await requireAuth(req, { minRole: 'free', endpoint: 'price/projection' });
  if (authErr) return authError(res, authErr);

  const { address, purchasePrice, purchaseMonth } = req.body || {};

  if (!address || typeof address !== 'string' || !address.trim()) {
    return res.status(400).json({ error: 'address is required' });
  }
  if (!purchasePrice || isNaN(Number(purchasePrice)) || Number(purchasePrice) <= 0) {
    return res.status(400).json({ error: 'purchasePrice is required and must be a positive number' });
  }
  if (!purchaseMonth || !/^[A-Z][a-z]{2}-\d{4}$/.test(purchaseMonth)) {
    return res.status(400).json({ error: 'purchaseMonth is required in format Mon-YYYY e.g. Jan-2015' });
  }

  try {
    const metro      = await detectMetro(address.trim());
    const projection = buildProjection(Number(purchasePrice), purchaseMonth, metro.fredCode);

    if (!projection.length) {
      return res.status(400).json({ error: 'purchaseMonth is outside the supported range (Jan 2000 – May 2027)' });
    }

    return res.status(200).json({
      metro:      metro.key,
      fredCode:   metro.fredCode,
      color:      metro.color,
      projection,
    });
  } catch (e) {
    return res.status(500).json({ error: e.message });
  }
}
