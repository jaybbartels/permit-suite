// ── Distance calculation (Haversine) ─────────────────────────────────────────
function haversineDistance(lat1, lng1, lat2, lng2) {
  const R = 3958.8; // Earth radius in miles
  const dLat = (lat2 - lat1) * Math.PI / 180;
  const dLng = (lng2 - lng1) * Math.PI / 180;
  const a =
    Math.sin(dLat / 2) * Math.sin(dLat / 2) +
    Math.cos(lat1 * Math.PI / 180) * Math.cos(lat2 * Math.PI / 180) *
    Math.sin(dLng / 2) * Math.sin(dLng / 2);
  return R * 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
}

// ── Geocode address via Nominatim (free, no key, server-side only) ────────────
async function geocodeAddress(address) {
  try {
    const encoded = encodeURIComponent(address);
    const res = await fetch(
      `https://nominatim.openstreetmap.org/search?q=${encoded}&format=json&limit=1`,
      {
        headers: { 'User-Agent': 'PermitSuite/1.0 (contact@domusai.com)' },
        signal: AbortSignal.timeout(5000),
      }
    );
    if (!res.ok) return null;
    const data = await res.json();
    if (!data || data.length === 0) return null;
    return { lat: parseFloat(data[0].lat), lng: parseFloat(data[0].lon) };
  } catch {
    return null;
  }
}

// ── ZIP code fast-path before geocoding ──────────────────────────────────────
function detectMetroByZip(address) {
  const zipMatch = address.match(/\b(\d{5})\b/);
  if (!zipMatch) return null;
  const zip = zipMatch[1];
  // Try longest prefix match first (3-digit), then 2-digit
  for (const metro of METROS) {
    for (const prefix of metro.proximityZips) {
      if (zip.startsWith(prefix)) return metro;
    }
  }
  return null;
}

// ── Main metro detection ──────────────────────────────────────────────────────
// 1. Fast ZIP prefix match
// 2. Geocode + haversine if ZIP misses
// 3. National fallback
async function detectMetro(address) {
  // Fast path — ZIP prefix
  const byZip = detectMetroByZip(address);
  if (byZip) return byZip;

  // Geocode fallback
  const coords = await geocodeAddress(address);
  if (coords) {
    let nearest = null;
    let nearestDist = Infinity;
    for (const metro of METROS) {
      const dist = haversineDistance(coords.lat, coords.lng, metro.lat, metro.lng);
      if (dist < metro.radius && dist < nearestDist) {
        nearest = metro;
        nearestDist = dist;
      }
    }
    if (nearest) return nearest;
  }

  // National fallback
  return { key: 'National', fredCode: 'CSUSHPISA', color: '#888780', lat: 39.5, lng: -98.35, radius: 0, proximityZips: [] };
}

